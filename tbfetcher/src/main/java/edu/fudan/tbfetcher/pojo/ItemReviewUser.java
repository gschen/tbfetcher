package edu.fudan.tbfetcher.pojo;

public class ItemReviewUser {
	private String vip;
	private int rank;
	private String nick;
	private int userId;
	private String displayRatePic;
	private String nickUrl;
	private int vipLevel;
	private String avatar;
	private boolean anony;
	private String rankUrl;
	public String getVip() {
		return vip;
	}
	public void setVip(String vip) {
		this.vip = vip;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getDisplayRatePic() {
		return displayRatePic;
	}
	public void setDisplayRatePic(String displayRatePic) {
		this.displayRatePic = displayRatePic;
	}
	public String getNickUrl() {
		return nickUrl;
	}
	public void setNickUrl(String nickUrl) {
		this.nickUrl = nickUrl;
	}
	public int getVipLevel() {
		return vipLevel;
	}
	public void setVipLevel(int vipLevel) {
		this.vipLevel = vipLevel;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	public boolean isAnony() {
		return anony;
	}
	public void setAnony(boolean anony) {
		this.anony = anony;
	}
	public String getRankUrl() {
		return rankUrl;
	}
	public void setRankUrl(String rankUrl) {
		this.rankUrl = rankUrl;
	}
	
	
	
	
}
